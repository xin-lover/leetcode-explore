vector<double> sampleStats(vector<int>& count) {
    //最小值，最大值，平均值，众数都很简单
    //中位数要先知道总的数量才能知道

    int mn=255,mx=0;
    double avg=0;
    int modeNum=0,mxCount=0;
    double sum = 0;
    int numCount = 0;
    double mid = 0;

    for(int i=0;i<count.size();++i)
    {
        if(count[i] > 0)
        {
            mn = min(mn,i);
            mx = max(mx,i);

            if(count[i] > mxCount)
            {
                modeNum = i;
                mxCount = count[i];
            }

            sum += i * count[i];
            numCount += count[i];
        }
    }

    avg = sum / numCount;

    int half = numCount / 2;
    int t = 0;
    int d1 = -1;
    for(int i=0;i < count.size();++i)
    {
        if(count[i] > 0)
        {
            if(t + count[i] > half)
            {
                if(d1 > -1)
                {
                    mid = (d1 + i) / 2.0;
                }
                else
                {
                    mid = i;
                }
                break;
            }
            else if(t + count[i] == half)
            {
                if((numCount & 1) == 0)
                {
                    d1 = i;
                }
            }
            t += count[i];
        }
    }

    return {mn,mx,avg,mid,modeNum};
}

